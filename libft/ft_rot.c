char	*ft_rot(char *str, int rot)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			str[i] -= 'a';
			str[i] = ((str[i] + rot) % 26) + 'a';
		}
		else if ('A' <= str[i] && str[i] <= 'Z')
		{
			str[i] -= 'A';
			str[i] = ((str[i] + rot) % 26) + 'A';
		}
		i++;
	}
	return (str);
}
